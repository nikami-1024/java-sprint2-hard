public class Delivery {
    public String title;
    public int count;
    public int year, month, day;
    public String city;

    public Delivery(String title, int count, int year, int month, int day, String city) {
        this.title = title;
        this.count = count;
        this.year = year;
        this.month = month;
        this.day = day;
        this.city = city;
    }
}
